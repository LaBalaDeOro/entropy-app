<h1 class="text-3xl font-bold mb-4">Privacy Policy</h1>
<p class="mb-4"><strong>Effective Date:</strong> May 5, 2025</p>

<p class="mb-4">
  The Ashlar Genesis app, developed by Just Entropy, Inc., uses Bluetooth and
  Wi-Fi to securely configure your Ashlar device for the ENTROPY network. During
  setup, the app collects and transmits the following data:
</p>

<ul class="list-disc list-inside mb-4 space-y-2">
  <li>
    <strong>Wi-Fi credentials</strong> &mdash; used only to configure the Ashlar
    to the Internet
  </li>
  <li>
    <strong>Solana public address</strong> &mdash; used to associate the device with
    a blockchain address
  </li>
</ul>

<p class="mb-4">
